#!/bin/bash

SEP="------------------------------------------------------------------"

echo "${SEP}"
echo "Спроба читання /etc/shadow від імені адміністратора..."
echo "${SEP}"

sudo cat /etc/shadow

if [ $? -eq 0 ]; then
    echo "${SEP}"
    echo "Файл успішно прочитано."
else
    echo "${SEP}"
    echo "Помилка: доступ відхилено або пароль введено неправильно."
fi

exit 0